    public function block($id)
    {
        $user = User::findorfail($id);
        if($user->is_blocked==1){
            $user->is_blocked = 0;
            $user->save();
        }
        else{
            $user->is_blocked = 1;
            $user->save();
        }
        return redirect()->route('admin.index');
    }
